package lista1;

public class Ex7_Tabuada {
    public void mostrarTabuada(int n) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + (n * i));
        }

        // Comentário: O programa usa um loop para multiplicar o número por 0 até 10 e exibe o resultado formatado.
    }
}
